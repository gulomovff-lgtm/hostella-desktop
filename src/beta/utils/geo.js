export const countryCode = (name) => {
    if (!name) return '—';
    const n = String(name).trim();
    if (RU2CODE[n]) return RU2CODE[n];
    // Латиница на входе — берём первые 2 буквы; кириллица — тоже, но кратко
    return n.slice(0, 2).toUpperCase();
};

// Местные (безвизовые СНГ, не требуют E-mehmon-строгостей) vs иностранцы — грубая эвристика
export const isLocalCountry = (name) => ['Узбекистан'].includes(String(name || '').trim());

// Сводка по гражданствам активных гостей: [{ country, code, count }] по убыванию
export const citizenshipBreakdown = (guests) => {
    const map = new Map();
    guests.filter(g => g.status === 'active').forEach(g => {
        const c = (g.country || '').trim() || 'Не указано';
        map.set(c, (map.get(c) || 0) + 1);
    });
    return [...map.entries()]
        .map(([country, count]) => ({ country, code: countryCode(country), count }))
        .sort((a, b) => b.count - a.count);
};
